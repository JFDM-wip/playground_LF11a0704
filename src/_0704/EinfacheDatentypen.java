package _0704;

public class EinfacheDatentypen {
    public static void main(String[] args) {
        // Primitive/einfache Datentypen

        // Ganzzahlige Datentypen
        // byte, short, int, long
        byte kleinsteGanzzahl;
        kleinsteGanzzahl = 1;
        short kleinereGanzzahl = 10;
        int normaleZahl = 100;
        long grosseZahl = 1_000_000L;

        // Gleitkommazahlen Datentypen
        // float, double
        float gleitkommaZahl1 = 10.00F;
        double gleitkommaZahl2 = 100.2323123;

        // Zeichen Datentyp
        // char wird nur mit '' (einfachen Hochkommata) dargestellt
        char einzelnesZeichen = 'a';

        // Wahrheitswerte boolescher Datentyp
        boolean wahrheitswert = true; // oder false

        //    var zahl = 10;

        // Variablen Deklaration
        // Datentyp variablenBezeichner
        // Variablen initialisierung
        // variablenBezeichner = WERT;
        int a, b, c;
        a = 4;
        b = 5;
        c = a;


    }
}
